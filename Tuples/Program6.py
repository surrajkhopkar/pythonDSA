# nested list
lst = [[1,2,3],["abc",3.14,"def"],456,["joe","bidden","usa"]]
print(lst[0])
print(lst[1])
print(lst[2])
print(lst[3])
print(lst[1][1])

# nested tuple
tup = ((1,2,3),("abd",3.14,"def"),456,["joe","bidden","usa"])
print(tup[0])
print(tup[1])
print(tup[2])
print(tup[3])
print(tup[1][1:])